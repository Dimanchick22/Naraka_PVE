"""
Вспомогательные функции для управления фокусом в приложении.
"""


def add_focus_handler(frame):
    """
    Добавляет обработчик клика для снятия фокуса с элементов ввода.

    Args:
        frame: Фрейм, к которому добавляется обработчик
    """

    def on_click(event):
        # Снимаем фокус с текущего активного виджета
        frame.focus_set()

    # Привязываем обработчик к событию клика левой кнопкой мыши
    frame.bind("<Button-1>", on_click)

    # Также делаем фрейм "фокусируемым"
    frame.configure(takefocus=True)